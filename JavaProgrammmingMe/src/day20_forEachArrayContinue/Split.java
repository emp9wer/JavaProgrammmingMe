package src.day20_forEachArrayContinue;

public class Split {
    public static void main(String[] args) {

        String words = "java is fun";

        String[] w = words.split(" ");

        System.out.println(w[0]); // java
        System.out.println(w[1]); // is
        System.out.println(w[2]); // fun




    }
}
