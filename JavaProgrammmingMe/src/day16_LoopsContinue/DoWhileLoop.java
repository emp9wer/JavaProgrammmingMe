package src.day16_LoopsContinue;

public class DoWhileLoop {
    public static void main(String[] args) {

        boolean condition = false;

        for(; condition ;){
            System.out.println("1111 for loop");
        }

        System.out.println("--------------------");

        while (condition){
            System.out.println("111111 while loop");
        }

        do {
            System.out.println("11111 do while loop");
        }while (condition);









    }
}
